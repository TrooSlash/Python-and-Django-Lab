"""
Лабораторная работа №5-6 - Настройка админ-панели

Этот файл настраивает как модели будут отображаться в админ-панели Django.
Django автоматически создает веб-интерфейс для управления данными в базе,
но его можно настроить под свои нужды.

Админ-панель доступна по адресу: /admin/
Логин/пароль: admin/admin
"""

from django.contrib import admin
from .models import Article  # Импортируем нашу модель Article


class ArticleAdmin(admin.ModelAdmin):
    """
    Настройка отображения модели Article в админ-панели.
    
    list_display - какие поля показывать в списке статей.
    Можно указать как поля модели, так и методы (например get_excerpt).
    
    Без этой настройки Django показывал бы просто "Article object (1)".
    """
    
    # Показываем в списке: заголовок, автор, превью текста и дату
    list_display = ('title', 'author', 'get_excerpt', 'created_date')


# Регистрируем модель в админке
# Первый аргумент - модель, второй - класс с настройками
admin.site.register(Article, ArticleAdmin)
