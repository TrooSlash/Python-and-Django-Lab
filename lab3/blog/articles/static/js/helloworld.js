/**
 * Функция rpad - дополняет строку пробелами справа до нужной длины
 * Нужна для красивого выравнивания таблицы в консоли
 * 
 * @param str - исходная строка
 * @param length - до какой длины дополнить
 * @return строка дополненная пробелами
 */
function rpad(str, length) {
    // Пока длина строки меньше нужной - добавляем пробел
    while (str.length < length) {
        str = str + " ";
    }
    return str;
}


/**
 * Функция printStudents - выводит список студентов в виде таблицы
 * Использует console.log для вывода в консоль браузера
 * 
 * @param students - массив студентов
 */
function printStudents(students) {
    // Выводим шапку таблицы
    console.log(rpad("Имя", 15) + rpad("Фамилия", 15) + rpad("Группа", 10) + "Оценки");
    // Выводим линию-разделитель (60 знаков =)
    console.log("=".repeat(60));
    
    // Проходим по всем студентам с помощью цикла for
    for (var i = 0; i < students.length; i++) {
        var student = students[i];
        // Формируем строку с данными студента
        var line = rpad(student.name, 15) + 
                   rpad(student.surname, 15) + 
                   rpad(student.group, 10) + 
                   student.marks.join(", ");  // join соединяет массив в строку через запятую
        console.log(line);
    }
}


